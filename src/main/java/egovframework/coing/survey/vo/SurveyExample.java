package egovframework.coing.survey.vo;

public class SurveyExample {

	private int svexId;
	
	private int svinId;
	
	private int svquId;
	
	private int svexKey;
	
	private String svexTitle;
	
	private String svexDelId;
	
	private String svexDelIp;

	public int getSvexId() {
		return svexId;
	}

	public void setSvexId(int svexId) {
		this.svexId = svexId;
	}

	public int getSvinId() {
		return svinId;
	}

	public void setSvinId(int svinId) {
		this.svinId = svinId;
	}

	public int getSvquId() {
		return svquId;
	}

	public void setSvquId(int svquId) {
		this.svquId = svquId;
	}

	public int getSvexKey() {
		return svexKey;
	}

	public void setSvexKey(int svexKey) {
		this.svexKey = svexKey;
	}

	public String getSvexTitle() {
		return svexTitle;
	}

	public void setSvexTitle(String svexTitle) {
		this.svexTitle = svexTitle;
	}

	public String getSvexDelId() {
		return svexDelId;
	}

	public void setSvexDelId(String svexDelId) {
		this.svexDelId = svexDelId;
	}

	public String getSvexDelIp() {
		return svexDelIp;
	}

	public void setSvexDelIp(String svexDelIp) {
		this.svexDelIp = svexDelIp;
	}

}